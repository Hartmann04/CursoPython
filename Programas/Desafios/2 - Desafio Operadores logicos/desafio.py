# Trabalhos

Trabalho_terca = True

Trabalho_quinta = False
# Sinta-se avontade para trocar para True ou False
Tv_50 = Trabalho_quinta and Trabalho_terca

sorvete = Trabalho_quinta or Trabalho_terca

Tv_32 = Trabalho_quinta != Trabalho_terca

mais_saudavel = not sorvete
#               1           2             3                   4
print("Tv_50 = {}, Tv_32 = {}, sorvete = {}, mais_saudavel = {}".format(Tv_50, Tv_32, sorvete, mais_saudavel))
