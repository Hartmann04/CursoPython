'''
Tipo operacionais
• 2+2
• 5-3
• 2-7
• 2*3
• 2*3.3
• 10/3
• 10//3
• 10%3
• 3**2

Segue a baixo todos os exemplo operacionais:
'''
print(2+2) # Retorna valor 4(INT) uma soma comum.

print(5-3) # Retorna valor 2(INT) Subtração comum.

print(2-7) # Retorna valor -5(INT) Tambem uma subtração comum.

print(2*3) # Retorna valor 6(INT) Multiplicação comum.

print(2*3.2) # Retorna valor 6.4(FLOAT) Multiplicação comum com decimal.

'''
 Quando se trata de divisão temos varios casos, podemos ter valores inteiros ou
 valores float e tambem è possivel retornar uma divisão cujo valor de uma dizima em um valor ""simples"" 
'''
print(10/3) # Retorna valor 3,333333 (FLOAT) divisão comum.

#Caso queira que a divisão retorne por exemplo 3 ou 3.0 deve se usar // 

print(10//3) # Retorna valor 3.0 ou 3 (FLOAT , INT) Divisão para numeros ""simples"" sem uma dizima por exemplo.

## modulo da divisão, retorna apenas o resto da divisão

print(10%3) # Retorna valor 1(INT), Retorna apenas o resto da divisão

print(2**3) # Retorna valor 8(INT) Potencia, neste caso potendia de 2³

